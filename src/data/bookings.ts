export const getCustomerCountByPropertyId = (propertyId: number, propertyName?: string, workspaceType?: string, city?: string): number => {
  return getCustomersByPropertyId(propertyId, propertyName, workspaceType, city).length;
};

// Helper function to calculate booking stats for a customer
export const getCustomerBookingStats = (customerId: number) => {
  const customerBookings = getBookingsByCustomerId(customerId);
  const totalBookings = customerBookings.length;
  const totalSpent = customerBookings.reduce((sum, booking) => sum + booking.billing.total, 0);
  const lastBookingDate = customerBookings.length > 0
    ? customerBookings.sort((a, b) => new Date(b.createdAt).getTime() - new Date(a.createdAt).getTime())[0].createdAt
    : null;
  
  return {
    totalBookings,
    totalSpent,
    lastBookingDate,
  };
};

