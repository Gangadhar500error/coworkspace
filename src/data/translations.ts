export const filterTranslations = (
  translations: Translation[],
  searchTerm: string,
  filters?: {
    languageCode?: string;
    group?: string;
    isActive?: string;
  }
): Translation[] => {
  let filtered = translations;
  
  if (searchTerm) {
    const term = searchTerm.toLowerCase();
    filtered = filtered.filter(
      (translation) =>
        translation.key.toLowerCase().includes(term) ||
        translation.value.toLowerCase().includes(term) ||
        translation.languageName.toLowerCase().includes(term) ||
        translation.group.toLowerCase().includes(term) ||
        translation.description?.toLowerCase().includes(term)
    );
  }
  
  if (filters) {
    if (filters.languageCode) {
      filtered = filtered.filter((translation) => translation.languageCode === filters.languageCode);
    }
    
    if (filters.group) {
      filtered = filtered.filter((translation) => translation.group === filters.group);
    }
    
    if (filters.isActive === "active") {
      filtered = filtered.filter((translation) => translation.isActive === true);
    } else if (filters.isActive === "inactive") {
      filtered = filtered.filter((translation) => translation.isActive === false);
    }
  }
  
  return filtered;
};

