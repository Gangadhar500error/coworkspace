/**
 * Filter properties based on search term and filters
 * Backend should implement similar filtering logic
 */
export const filterCustomerProperties = (
  properties: CustomerProperty[],
  searchTerm: string,
  filters: {
    status: string;
    workspaceType: string;
    verificationStatus: string;
  }
): CustomerProperty[] => {
  let filtered = properties;

  // Search filter
  if (searchTerm) {
    const term = searchTerm.toLowerCase();
    filtered = filtered.filter(
      (property) =>
        property.propertyName.toLowerCase().includes(term) ||
        property.location.toLowerCase().includes(term) ||
        property.city.toLowerCase().includes(term) ||
        property.workspaceType.toLowerCase().includes(term)
    );
  }

  // Status filter
  if (filters.status && filters.status !== "all") {
    filtered = filtered.filter((property) => property.status === filters.status);
  }

  // Workspace type filter
  if (filters.workspaceType && filters.workspaceType !== "all") {
    filtered = filtered.filter((property) => property.workspaceType === filters.workspaceType);
  }

  // Verification status filter
  if (filters.verificationStatus && filters.verificationStatus !== "all") {
    filtered = filtered.filter((property) => property.verificationStatus === filters.verificationStatus);
  }

  return filtered;
};

